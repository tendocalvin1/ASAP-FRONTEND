// Loops in JavaScript
// while loop

let counter = 1;
while (counter < 10){
    counter++;
    console.log(`Counter is at: ${counter}`);
}

let i = 0;
while (i < 5){
    i++;
    console.log(`The number is now :${i}`);
}

// for loop
for (let m = 7; m <= 10; m++){
    console.log(`The number is now: ${m}`);
}