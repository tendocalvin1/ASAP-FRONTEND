// Functions in JavaScript
// A function is a reusable block of code. 

function sayHello(name){
    console.log(`Hello, ${name}. I want to be employed as a full stack developer by June 2026.`)
}

sayHello("Tendo");

function addNumbers(num1, num2){
    console.log(`The sum of these numbers is : ${num1 + num2}`);
}

function multiplyNumbers(num1, num2){
    console.log(`The product of these numbers is : ${num1 * num2}`);
}

function subtractNumbers(num1, num2){
    console.log(`The difference of these numbers is : ${num1 - num2}`);
}

function divisionNumbers(num1, num2){
    console.log(`The result of these numbers is : ${num1 / num2}`);
}

addNumbers(2,4)
multiplyNumbers(2,4)
subtractNumbers(2,4)
divisionNumbers(2,4)